export function tl(lang: Lang): typeof translations.no {
  return translations[lang];
}

export function greeting(lang: Lang): string {
  const h = new Date().getHours();
  if (h < 6) return translations[lang].greeting.night;
  if (h < 12) return translations[lang].greeting.morning;
  if (h < 18) return translations[lang].greeting.afternoon;
  return translations[lang].greeting.evening;
}
